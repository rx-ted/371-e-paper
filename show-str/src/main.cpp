void lcd_init()
{
  // 240H*416V
  lcd_reset();
  delay(100);
  lcd_write_cmd(0x06);
  lcd_write_data(0x17); // 10111b
  lcd_write_data(0x17);
  lcd_write_data(0x1d);
  lcd_write_cmd(0x04);
  lcd_check_status();
  lcd_write_cmd(0x00);
  lcd_write_data(0x1f);
  lcd_write_cmd(0x61);
  lcd_write_data(0xf0); // 240
  lcd_write_data(0x01); // 416 =  1,1010,0000
  lcd_write_data(0xa0);
  lcd_write_cmd(0x50);
  lcd_write_data(0x23);
  lcd_write_data(0x07);
}
void lcd_write_data(unsigned char cmd)
{
  CS_0;
  DC_1;
  SPI.transfer(cmd);
  CS_1;
}
void lcd_write_cmd(unsigned char cmd)
{

  CS_0;
  DC_0;
  SPI.transfer(cmd);
  CS_1;
}
void lcd_reset()
{
  RES_0;
  delay(10);
  RES_1;
  delay(10);
}
void lcd_check_status()
{
  while (1)
  {
    if (IS_BUSY == 1)
    {
      break;
    }
    ESP.wdtFeed();
  }
}
void lcd_refresh()
{
  lcd_write_cmd(0x12);
  delay(100);
  lcd_check_status();
}

void lcd_sleep()
{
  lcd_write_cmd(0x50);
  lcd_write_data(0xf7);
  lcd_write_cmd(0x02);
  lcd_check_status();
  lcd_write_cmd(0x07);
  lcd_write_data(0xa5);
}

void lcd_clean()
{
  unsigned int i;
  lcd_write_cmd(0x10);
  for (i = 0; i < 12480; i++)
  {
    lcd_write_data(0xff);
  }
  lcd_write_cmd(0x13);
  for (i = 0; i < 12480; i++)
  {
    lcd_write_data(0xff);
  }
}

